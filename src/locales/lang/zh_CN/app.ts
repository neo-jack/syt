// 定义一个名为 'app' 的对象。这个对象主要用来存放我们网站应用的一些基本文本信息。
// 'const' 是一个 JavaScript 关键字，它声明了一个只读的常量。一旦被赋值后，常量的值就不能再改变。
// 这样做可以防止在程序的其他地方意外地修改了这个重要的配置信息，让代码更可靠。
// 从文件名中的 zh_CN 可以看出，这个文件是专门存放简体中文版本的文本的。
const app = {
  // 在 'app' 对象内部，我们定义了一个属性，它的“键”（key）是 'dashboard'，“值”（value）是 "首页"。
  // 你可以把“键”想象成一个标签名，“值”就是这个标签对应的内容。
  // 在程序中，当需要显示“首页”这个词的时候，开发者会使用 'dashboard' 这个键来获取到 "首页" 这段文字。
  // 这种键值对的写法是“国际化”（i18n）的一种常见实践，方便未来支持多种语言。
  dashboard: "首页",

  // 这里我们定义了另一个属性，键是 'title'，对应的值是 "商医通管理系统"。
  // 这个是整个管理系统的名称。
  // 当网页的标题或者其他地方需要显示系统名称时，就会使用 'title' 这个键。
  title: "商医通管理系统",
};

// 'export default' 是 ES6 模块系统中的一个命令，用于导出一个模块的默认值。
// 在这里，它把我们刚刚创建的 'app' 对象作为这个文件（模块）的默认导出项。
// “导出”之后，项目中的其他文件就可以通过 'import' 语句来“导入”并使用这个 'app' 对象了。
// 比如，其他文件可以通过 `import app from '...'` 来引用这个对象，然后通过 `app.title` 来获取系统标题。
// 这样做可以很好地组织代码，让不同功能的代码分离开，使项目结构更清晰，易于维护。
export default app;
